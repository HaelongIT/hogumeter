-- Q-66 ①: 딜이 수요축 값을 지닌다.
--
-- 확정본 §40·41: 수요축 묶음/분리는 사용자 토글(기본 묶음)이고, **분리 시 글에서 축 값을 판별**한다.
-- 판별 못 한 딜은 "값 미상" 버킷 → 기준가 계산 제외, 승격 큐에서 사람이 분류.
-- 그러려면 딜마다 "어느 수요축 값인가"가 남아야 한다. 지금까지 그 자리가 없어 SPLIT은 저장만 되고
-- 아무 동작도 바꾸지 못했다(수요축 기능 전체가 죽어 있었다).
--
-- null = **값 미상**이다. 세 경우가 같은 null로 온다:
--   ① 수요축을 등록하지 않은 제품(대부분) ② 제목에 값이 없음 ③ 값이 둘 이상 보여 어느 것인지 모름.
-- 굳이 구분하지 않는 이유: 어느 쪽이든 "SPLIT 분포에 넣을 수 없다"는 결론이 같다. GROUPED 제품에선
-- 이 값이 분포를 가르지 않으므로 null이어도 아무 손해가 없다.
--
-- 판별은 모드와 무관하게 항상 한다 — 나중에 묶음→분리로 바꿔도 과거 딜의 값이 남아 있어야 한다.
alter table deal_event
    add column demand_axis_value text;

-- SPLIT 조회는 (variant, 수요축 값)으로 표본을 고른다. 1인용 규모라 인덱스는 이거면 족하다.
create index idx_deal_event_variant_demand on deal_event (variant_id, demand_axis_value);
